const { EmbedBuilder, ActionRowBuilder, StringSelectMenuBuilder, SlashCommandBuilder } = require('discord.js');

module.exports = {
    name: 'help',
    description: 'Muestra informaci√≥n del bot y lista de comandos disponibles',
    aliases: ['ayuda', 'comandos', 'botinfo', "comando", "commands", "botayuda", "botcomandos", "bothelp", "h", "cmds", "cmd"],
    usage: 'cz!help [comando]',
    category: 'Informaci√≥n',
    
    // Definici√≥n del slash command
    slashCommand: new SlashCommandBuilder()
        .setName('help')
        .setDescription('Muestra informaci√≥n del bot y lista de comandos disponibles')
        .addStringOption(option =>
            option.setName('comando')
                .setDescription('Nombre del comando para obtener informaci√≥n detallada')
                .setRequired(false)),
    
    async execute(message, args) {
        const { commands } = message.client;
        
        // Si se solicita un comando espec√≠fico
        if (args.length > 0) {
            const commandName = args[0].toLowerCase();
            const command = commands.get(commandName) || 
                           commands.find(cmd => cmd.aliases && cmd.aliases.includes(commandName));
            
            if (!command) {
                const errorEmbed = new EmbedBuilder()
                    .setColor('#4f46e5')
                    .setTitle('‚ùå Comando no encontrado')
                    .setDescription(`No se encontr√≥ el comando \`${commandName}\``)
                    .addFields({
                        name: 'üí° Sugerencia',
                        value: 'Usa `cz!help` para ver todos los comandos disponibles',
                        inline: false
                    })
                    .setFooter({
                        text: 'CiszuBot',
                        iconURL: message.client.user.displayAvatarURL()
                    })
                    .setTimestamp();
                
                return await message.reply({ embeds: [errorEmbed] });
            }
            
            // Crear embed detallado para comando espec√≠fico
            const embed = new EmbedBuilder()
                .setColor('#4f46e5')
                .setTitle(`üìñ Comando: ${command.name}`)
                .setDescription(command.description || 'Sin descripci√≥n disponible')
                .addFields(
                    {
                        name: 'üìù Uso',
                        value: `\`${command.usage || `cz!${command.name}`}\``,
                        inline: true
                    },
                    {
                        name: 'üìÇ Categor√≠a',
                        value: command.category || 'General',
                        inline: true
                    }
                );
            
            if (command.aliases && command.aliases.length > 0) {
                embed.addFields({
                    name: 'üîó Aliases',
                    value: command.aliases.map(alias => `\`${alias}\``).join(', '),
                    inline: false
                });
            }
            
            embed.setFooter({
                text: `CiszuBot ‚Ä¢ Solicitado por ${message.author.tag}`,
                iconURL: message.client.user.displayAvatarURL()
            }).setTimestamp();
            
            return await message.reply({ embeds: [embed] });
        }
        
        // Crear embed general de ayuda
        const totalCommands = commands.size;
        let description = '';
        let serverInfo = '';
        
        if (message.guild) {
            const totalServers = message.client.guilds.cache.size;
            description = `¬°Hola! Soy CiszuBot, un bot completamente en espa√±ol. Tengo **${totalCommands} comandos** disponibles y estoy sirviendo en **${totalServers} servidor(es)**.`;
            serverInfo = `‚Ä¢ Servidores: ${totalServers}\n`;
        } else {
            description = `¬°Hola! Soy CiszuBot, un bot completamente en espa√±ol. Tengo **${totalCommands} comandos** disponibles.`;
        }
        
        const embed = new EmbedBuilder()
            .setColor('#4f46e5')
            .setTitle('ü§ñ CiszuBot - Ayuda y Comandos')
            .setDescription(description)
            .setThumbnail(message.client.user.displayAvatarURL())
            .setFooter({
                text: `CiszuBot ‚Ä¢ Solicitado por ${message.author.tag}`,
                iconURL: message.client.user.displayAvatarURL()
            })
            .setTimestamp();
        
        // Agrupar comandos por categor√≠a
        const categories = {};
        commands.forEach(command => {
            const category = command.category || 'General';
            if (!categories[category]) {
                categories[category] = [];
            }
            categories[category].push(command);
        });
        
        // A√±adir campos para cada categor√≠a
        Object.keys(categories).forEach(category => {
            if (categories[category].length > 0) {
                const commandList = categories[category]
                    .map(cmd => `\`cz!${cmd.name}\` - ${cmd.description || 'Sin descripci√≥n'}`)
                    .join('\n');
                
                embed.addFields({
                    name: `üìÅ ${category}`,
                    value: commandList,
                    inline: false
                });
            }
        });
        
        // A√±adir informaci√≥n general
        embed.addFields(
            {
                name: 'üîß Informaci√≥n del Bot',
                value: `‚Ä¢ Prefijo: \`cz!\`\n‚Ä¢ Comandos: ${totalCommands}\n${serverInfo}‚Ä¢ Idioma: Espa√±ol`,
                inline: true
            },
            {
                name: 'üåü Consejos R√°pidos',
                value: '‚Ä¢ Usa `cz!help <comando>` para info detallada\n‚Ä¢ Puedes mencionarme en lugar del prefijo\n‚Ä¢ Los comandos no distinguen may√∫sculas',
                inline: true
            }
        );
        
        // Crear men√∫ de selecci√≥n para acceso r√°pido a comandos (solo en servidores)
        let components = [];
        if (message.guild) {
            const selectMenu = new StringSelectMenuBuilder()
                .setCustomId('help_select')
                .setPlaceholder('Selecciona un comando para m√°s informaci√≥n')
                .addOptions(
                    Array.from(commands.values())
                        .filter(command => command.name !== 'template') // Excluir template
                        .map(command => ({
                            label: command.name,
                            description: command.description || 'Sin descripci√≥n',
                            value: command.name,
                            emoji: this.getCommandEmoji(command.name)
                        }))
                );
            
            const row = new ActionRowBuilder()
                .addComponents(selectMenu);
            
            components = [row];
        }
        
        const response = await message.reply({
            embeds: [embed],
            components: components
        });
        
        // Manejar interacciones del men√∫ de selecci√≥n
        const filter = (interaction) => {
            return interaction.customId === 'help_select' && interaction.user.id === message.author.id;
        };
        
        const collector = response.createMessageComponentCollector({
            filter,
            time: 60000 // 1 minuto
        });
        
        collector.on('collect', async (interaction) => {
            const selectedCommand = commands.get(interaction.values[0]);
            
            if (!selectedCommand) {
                await interaction.reply({
                    content: '‚ùå ¬°Comando no encontrado!',
                    ephemeral: true
                });
                return;
            }
            
            const commandEmbed = new EmbedBuilder()
                .setColor('#4f46e5')
                .setTitle(`üìñ Comando: ${selectedCommand.name}`)
                .setDescription(selectedCommand.description || 'Sin descripci√≥n disponible')
                .addFields(
                    {
                        name: 'üìù Uso',
                        value: `\`${selectedCommand.usage || `cz!${selectedCommand.name}`}\``,
                        inline: true
                    },
                    {
                        name: 'üìÇ Categor√≠a',
                        value: selectedCommand.category || 'General',
                        inline: true
                    }
                );
            
            if (selectedCommand.aliases && selectedCommand.aliases.length > 0) {
                commandEmbed.addFields({
                    name: 'üîó Aliases',
                    value: selectedCommand.aliases.map(alias => `\`${alias}\``).join(', '),
                    inline: false
                });
            }
            
            commandEmbed.setFooter({
                text: `CiszuBot ‚Ä¢ Solicitado por ${interaction.user.tag}`,
                iconURL: message.client.user.displayAvatarURL()
            }).setTimestamp();
            
            await interaction.reply({
                embeds: [commandEmbed],
                ephemeral: true
            });
        });
        
        collector.on('end', async () => {
            // Deshabilitar el men√∫ de selecci√≥n despu√©s del timeout
            const disabledRow = new ActionRowBuilder()
                .addComponents(
                    StringSelectMenuBuilder.from(selectMenu)
                        .setDisabled(true)
                        .setPlaceholder('Men√∫ de selecci√≥n expirado')
                );
            
            try {
                await response.edit({ components: [disabledRow] });
            } catch (error) {
                // El mensaje podr√≠a haber sido eliminado
            }
        });
    },
    
    getCommandEmoji(commandName) {
        const emojiMap = {
            'ping': 'üèì',
            'pong': 'üèì',
            'help': 'üìñ',
            'say': 'üí¨',
            'directsay': 'üí¨',
            'confess': 'ü§´',
            'hi': 'üëã',
            'bye': 'üëã',
            'profile': 'üë§',
            'test': 'üß™',
            'info': '‚ÑπÔ∏è',
            'stats': 'üìä',
            'server': 'üè†',
            'user': 'üë§'
        };
        
        return emojiMap[commandName] || '‚ö°';
    }
};